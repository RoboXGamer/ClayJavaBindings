package ClayJava;

public class Clay_BoundingBox {
    public float x;
    public float y;
    public float width;
    public float height;
    
    Clay_BoundingBox(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
